.MODEL SMALL 
.STACK 100H 
.DATA 
	MSG DB "LARGEST NUMBER IS $"
	NUM DB 06H,08H,09H,10H,13H,12H,35H,21H,22H,30H 
	LG DB 0H 
.CODE 
	 MOV AX, @DATA 
	 MOV DS, AX 
	 MOV CX, 10
	 MOV AL,0H
	 LEA SI,NUM 
CHECK:	 MOV BL,[SI] 
   	 CMP AL,BL 
   	 INC SI 
	 JNC NOCARRY 
	 MOV AL,BL 
NOCARRY: DEC CX 
 	 JNZ CHECK 
 	 MOV LG, AL
	 LEA DX, MSG
         MOV AH, 09H
	 INT 21H 
         LEA DX, LG
         MOV AH, 09H
         INT 21H 
 	 MOV AH,4CH 
 	 INT 21H 
 	 END 
